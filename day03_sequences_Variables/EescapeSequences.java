package day03_sequences_Variables;

public class EescapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello everyone \nMy name is Cybertek");
        System.out.println("\tI am in NY\n");
        System.out.println("\t\t\t\n\nI love programming");
        System.out.println("\n\n\t\t\tI love programming");
        System.out.println("\\\\"); // this print me two back slash
        System.out.println("//"); //  this print me two front slash
        System.out.println("My name is \'Zebo\'"); // 'Zebo'
        System.out.println("My name is 'Zebo'");
        System.out.println("My favorite: \"Game of Thrones\"");




    }



    }




